// ── Simple text hash for DOM-scrape change detection ──────────────────────
function hashText(text) {
  let h = 0;
  for (let i = 0; i < Math.min(text.length, 2000); i++) {
    h = (Math.imul(31, h) + text.charCodeAt(i)) | 0;
  }
  return String(h);
}

// ── Friendly API error messages ───────────────────────────────────────────
function friendlyApiError(status, rawMsg) {
  if (status === 401) return 'API key rejected — check your Hyperspace key in Settings.';
  if (status === 403) return 'Access denied (403) — your proxy may not allow this request.';
  if (status === 429) return 'Rate limit reached — wait a moment and try again.';
  if (status >= 500)  return `Server error (${status}) — try again shortly.`;
  return rawMsg || `HTTP ${status}`;
}

// ── Markdown renderer ─────────────────────────────────────────────────────
function escapeHtml(s) {
  return s
    .replace(/&/g, '&amp;').replace(/</g, '&lt;')
    .replace(/>/g, '&gt;').replace(/"/g, '&quot;');
}

function inlineFormat(text) {
  return text
    .replace(/\*\*([^*\n]+)\*\*/g, '<strong>$1</strong>')
    .replace(/\*([^*\n]+)\*/g, '<em>$1</em>')
    .replace(/\[((?:[^\[\]]|\[[^\[\]]*\])*)\]\((https?:\/\/[^)]+)\)/g, '<a href="$2" target="_blank" rel="noopener">$1</a>');
}

// Strip markdown syntax for plain-text copy/post operations
function stripMarkdown(text) {
  if (!text) return '';
  return text
    .replace(/\*\*([^*\n]+)\*\*/g, '$1')
    .replace(/\*([^*\n]+)\*/g, '$1')
    .replace(/^#{1,3} /gm, '');
}

function renderMarkdown(text) {
  if (!text) return '';
  const out = [];
  let inList = false;
  let listTag = '';
  let paraLines = [];

  function flushPara() {
    if (!paraLines.length) return;
    out.push(`<p>${paraLines.join('<br>')}</p>`);
    paraLines = [];
  }
  function closeList() {
    if (inList) { out.push(`</${listTag}>`); inList = false; listTag = ''; }
  }

  for (const rawLine of text.split('\n')) {
    const trimmed = rawLine.trim();

    if (!trimmed) { flushPara(); closeList(); continue; }

    if (/^---+$/.test(trimmed)) {
      flushPara(); closeList();
      out.push('<hr>');
      continue;
    }

    const headingMatch = trimmed.match(/^(#{1,3}) (.+)/);
    if (headingMatch) {
      flushPara(); closeList();
      const tag = headingMatch[1].length <= 2 ? 'h3' : 'h4';
      out.push(`<${tag}>${inlineFormat(escapeHtml(headingMatch[2]))}</${tag}>`);
      continue;
    }

    const ulMatch = trimmed.match(/^[-*] (.+)/);
    if (ulMatch) {
      flushPara();
      if (listTag === 'ol') closeList();
      if (!inList) { out.push('<ul>'); inList = true; listTag = 'ul'; }
      out.push(`<li>${inlineFormat(escapeHtml(ulMatch[1]))}</li>`);
      continue;
    }

    const olMatch = trimmed.match(/^\d+\. (.+)/);
    if (olMatch) {
      flushPara();
      if (listTag === 'ul') closeList();
      if (!inList) { out.push('<ol>'); inList = true; listTag = 'ol'; }
      out.push(`<li>${inlineFormat(escapeHtml(olMatch[1]))}</li>`);
      continue;
    }

    closeList();
    paraLines.push(inlineFormat(escapeHtml(trimmed)));
  }
  flushPara();
  closeList();
  return out.join('');
}

