// ── Cache helpers ─────────────────────────────────────────────────────────
async function getCachedAnalysis(taskId) {
  const ttl = await getCacheTtl();
  return new Promise(resolve => {
    chrome.storage.local.get(['analysis_cache'], result => {
      if (chrome.runtime.lastError) { resolve(null); return; }
      const cache = result?.analysis_cache || {};
      const c = cache[taskId];
      if (c && (ttl === Infinity || Date.now() - c.timestamp < ttl)) {
        resolve(c);
      } else {
        resolve(null);
      }
    });
  });
}

function setCachedAnalysis(taskId, analysis, taskData, dataSource) {
  // Strip attachment blobs to keep storage size manageable
  const cacheableData = { ...taskData, attachmentContents: [] };
  chrome.storage.local.get(['analysis_cache'], result => {
    const cache = result.analysis_cache || {};
    cache[taskId] = {
      taskId, analysis, taskData: cacheableData, dataSource, timestamp: Date.now(),
      rawTextHash: dataSource === 'page' ? hashText(taskData.rawPageText || '') : null,
    };
    // Keep only the 10 most-recent entries to prevent storage bloat
    const sorted = Object.entries(cache).sort(([, a], [, b]) => b.timestamp - a.timestamp);
    chrome.storage.local.set({ analysis_cache: Object.fromEntries(sorted.slice(0, 10)) });
  });
}

