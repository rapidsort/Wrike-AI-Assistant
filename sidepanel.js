// ── Markdown → HTML for Wrike rich-text comments ──────────────────────────
function markdownToHtml(text) {
  if (!text) return '';
  const lines = text.split('\n');
  const out = [];
  let inUl = false, inOl = false;

  function closeLists() {
    if (inUl) { out.push('</ul>'); inUl = false; }
    if (inOl) { out.push('</ol>'); inOl = false; }
  }
  function inlineHtml(s) {
    return s
      .replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;')
      .replace(/\*\*([^*\n]+)\*\*/g, '<strong>$1</strong>')
      .replace(/\*([^*\n]+)\*/g,     '<em>$1</em>');
  }

  for (const raw of lines) {
    const t = raw.trim();
    if (!t) { closeLists(); continue; }

    const hm = t.match(/^(#{1,3}) (.+)/);
    if (hm) {
      closeLists();
      const tag = hm[1].length === 1 ? 'h2' : 'h3';
      out.push(`<${tag}>${inlineHtml(hm[2])}</${tag}>`);
      continue;
    }

    const ulm = t.match(/^[-*] (.+)/);
    if (ulm) {
      if (inOl) { out.push('</ol>'); inOl = false; }
      if (!inUl) { out.push('<ul>'); inUl = true; }
      out.push(`<li>${inlineHtml(ulm[1])}</li>`);
      continue;
    }

    const olm = t.match(/^\d+\. (.+)/);
    if (olm) {
      if (inUl) { out.push('</ul>'); inUl = false; }
      if (!inOl) { out.push('<ol>'); inOl = true; }
      out.push(`<li>${inlineHtml(olm[1])}</li>`);
      continue;
    }

    closeLists();
    out.push(`<p>${inlineHtml(t)}</p>`);
  }
  closeLists();
  return out.join('');
}

// ── Extract partial JSON string value during streaming ─────────────────────
function extractPartialValue(raw, storeKey) {
  const markers = [`"${storeKey}": "`, `"${storeKey}":"`];
  let idx = -1;
  for (const m of markers) {
    const i = raw.indexOf(m);
    if (i !== -1) { idx = i + m.length; break; }
  }
  if (idx === -1) return null;
  let value = raw.slice(idx).replace(/"?\s*\}?\s*$/, '');
  return value.replace(/\\n/g, '\n').replace(/\\t/g, '\t').replace(/\\"/g, '"').replace(/\\\\/g, '\\');
}

// ── Robust JSON parser — handles markdown fences, leading text, truncation ──
function parseAIJson(raw) {
  if (!raw) return null;
  // 1. Direct parse
  try { return JSON.parse(raw); } catch (_) {}
  // 2. Strip markdown code fences (```json ... ``` or ``` ... ```)
  const fenced = raw.match(/```(?:json)?\s*([\s\S]*?)```/);
  if (fenced) { try { return JSON.parse(fenced[1].trim()); } catch (_) {} }
  // 3. Greedy first JSON object
  const objMatch = raw.match(/\{[\s\S]*\}/);
  if (objMatch) { try { return JSON.parse(objMatch[0]); } catch (_) {} }
  // 4. Truncated JSON — the stream was cut off mid-string value.
  //    Find the last opening { and try to close the string + object.
  const openBrace = raw.indexOf('{');
  if (openBrace !== -1) {
    const inner = raw.slice(openBrace);
    // Try closing as: ...partial text"} and ...partial text\\n"}
    for (const closing of ['"}', '\\n"}', '"]}']) {
      try { return JSON.parse(inner + closing); } catch (_) {}
    }
  }
  return null;
}

// ── Find a storeKey value in a parsed object, tolerating AI key variations ──
// Handles: wrong casing, spaces-vs-underscores, nested wrapper objects,
// and custom tabs where the AI uses a different key name entirely.
function extractStoreValue(parsed, storeKey) {
  if (!parsed || typeof parsed !== 'object') return undefined;
  // 1. Exact match
  if (parsed[storeKey] !== undefined) return parsed[storeKey];
  // 2. Normalise: lowercase + strip non-alphanumeric
  const norm = s => s.toLowerCase().replace(/[^a-z0-9]/g, '');
  const target = norm(storeKey);
  for (const k of Object.keys(parsed)) {
    if (norm(k) === target) return parsed[k];
  }
  // 3. Nested single-key wrapper (e.g. {"result": {"next_steps": [...]}})
  const vals = Object.values(parsed);
  if (vals.length === 1 && vals[0] && typeof vals[0] === 'object' && !Array.isArray(vals[0])) {
    return extractStoreValue(vals[0], storeKey);
  }
  // 4. Last resort for custom tabs: AI used a different key name entirely.
  //    Accept the first string or array value in a single-key object.
  if (vals.length === 1 && (typeof vals[0] === 'string' || Array.isArray(vals[0]))) {
    return vals[0];
  }
  return undefined;
}

