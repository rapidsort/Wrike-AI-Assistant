// ── On-demand section generation ──────────────────────────────────────────
async function generateTabIfNeeded(tabId) {
  if (tabGenerated[tabId])  return;
  if (tabGenerating[tabId]) return;
  if (!conversationHistory.length) return;
  if (document.querySelector(`.tab-btn[data-tab="${tabId}"]`)?.classList.contains('hidden')) return;

  const meta = SECTION_META[tabId];
  if (!meta?.followupMsg) return;

  const tabStyle  = cachedTabStyles[tabId] || '';
  const styledMsg = meta.followupMsg + (tabStyle ? `\n\nStyle instructions: ${tabStyle}` : '');

  tabGenerating[tabId] = true;
  setTabBtnLoading(tabId, true);
  setTabState(tabId, 'loading');

  try {
    const s        = await getAiSettings();
    const profile  = await getProfile();
    const systemText = buildSystemPrompt(profile);
    const histMsgs   = [...textOnlyHistory(conversationHistory), { role: 'user', content: styledMsg }];

    // Continuation loop — if response is truncated (max_tokens hit), keep asking
    // the AI to continue until we have a complete, parseable JSON response.
    let raw = await callAIChat(s, systemText, histMsgs, meta.maxTokens, tabId);
    let continuations = 0;
    const MAX_CONT = 4;

    while (continuations < MAX_CONT) {
      const parsed = parseAIJson(raw);
      const value  = parsed !== null ? extractStoreValue(parsed, meta.storeKey) : undefined;
      if (value !== undefined) {
        // Successfully parsed — store and render
        if (!currentAnalysis) currentAnalysis = {};
        currentAnalysis[meta.storeKey] = value;
        conversationHistory.push({ role: 'user',      content: styledMsg });
        conversationHistory.push({ role: 'assistant', content: JSON.stringify(parsed) });
        setTabState(tabId, 'done');
        renderSection(tabId, value);
        renderSourcesLine(tabId);
        if (EDITABLE_TABS.has(tabId)) addToVersionHistory(tabId, value, 'Original');
        tabGenerated[tabId] = true;
        if (currentTaskData?.taskId) {
          setCachedAnalysis(currentTaskData.taskId, currentAnalysis, currentTaskData, currentDataSource);
        }
        if (tabId !== 'summary') logTelemetryEvent('TAB_GENERATED', {
          ticketId: currentTaskData?.taskId, ticketTitle: currentTaskData?.title, tab: tabId,
          aiProvider: await getAiProvider(), aiModel: cachedClaudeModel,
        });
        return;
      }

      // Truncated — ask AI to continue from where it left off
      continuations++;
      const contMsgs = [
        ...histMsgs,
        { role: 'assistant', content: raw },
        { role: 'user', content: 'Continue exactly from where you left off. Do not repeat any text already written. Complete the JSON.' },
      ];
      const spanEl = document.querySelector(`#tab-${tabId} .tab-loading span`);
      if (spanEl) spanEl.textContent = `Continuing… (part ${continuations + 1})`;
      const extra = await callAIChat(s, systemText, contMsgs, meta.maxTokens, null, null);
      raw = raw + extra;
    }

    throw new Error('Unexpected response — try again.');

  } catch (err) {
    setTabState(tabId, 'error', `${err.message} — click to retry.`);
  } finally {
    tabGenerating[tabId] = false;
    setTabBtnLoading(tabId, false);
  }
}

// ── Per-tab regeneration ──────────────────────────────────────────────────

// Rebuild conversationHistory[0] with the current context textarea, attached
// files, and fetched links so Regenerate always reflects the latest input.
function rebuildFirstHistoryMessage() {
  if (!currentTaskData || !conversationHistory.length) return;
  const extraContext  = getExtraContextValue();
  const ticketText    = buildUserPrompt(currentTaskData, extraContext);
  const sectionReq    = '\n\nReturn ONLY this JSON: {"summary": "..."} — 2–3 sentence overview of what this ticket is about and its current state.';
  conversationHistory[0] = {
    role   : 'user',
    content: buildMessageBlocks(currentTaskData, ticketText, sectionReq),
  };
}

async function regenerateTab(tabId) {
  if (tabGenerating[tabId]) return;
  if (!conversationHistory.length) return;

  setTabBtnLoading(tabId, true);

  if (EDITABLE_TABS.has(tabId)) {
    tabVersionHistory[tabId] = [];
    tabVersionCurrent[tabId] = -1;
    renderVersionBar(tabId);
  }

  // Always rebuild first message so current context/files/links are included
  rebuildFirstHistoryMessage();

  const meta = SECTION_META[tabId];

  if (tabId === 'summary') {
    // Re-call with just the original first user message to get a fresh summary
    tabGenerating[tabId] = true;
    setTabState(tabId, 'loading');
    try {
      const s        = await getAiSettings();
      const profile  = await getProfile();
      const raw = await callAIChat(
        s, buildSystemPrompt(profile), [conversationHistory[0]], meta.maxTokens, null, null
      );

      let parsed = parseAIJson(raw);
      const summary = parsed !== null ? extractStoreValue(parsed, 'summary') : undefined;
      if (!summary) throw new Error('Unexpected response — try again.');

      conversationHistory[1] = { role: 'assistant', content: JSON.stringify(parsed) };
      currentAnalysis.summary = summary;
      renderSection(tabId, summary);
      renderSourcesLine(tabId);
      setTabState(tabId, 'done');
      tabGenerated[tabId] = true;

      if (currentTaskData?.taskId) {
        setCachedAnalysis(currentTaskData.taskId, currentAnalysis, currentTaskData, currentDataSource);
      }
    } catch (err) {
      setTabState(tabId, 'error', `${err.message} — click to retry.`);
    } finally {
      tabGenerating[tabId] = false;
      setTabBtnLoading(tabId, false);
    }
  } else {
    // Remove the previous Q&A for this tab from conversationHistory, then re-generate
    const idx = conversationHistory.findIndex(
      msg => typeof msg.content === 'string' && msg.content === meta.followupMsg
    );
    if (idx !== -1) conversationHistory.splice(idx, 2);

    tabGenerated[tabId] = false;
    await generateTabIfNeeded(tabId);
    setTabBtnLoading(tabId, false);
  }
}

