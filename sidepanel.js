const TELEMETRY_FOLDER_ID = 'MQAAAAELm2NX';

async function getTelemetryTaskId(token, userName) {
  const storageKey = `waa_telemetry_task_${userName.replace(/\s+/g, '_')}`;
  const cached = await new Promise(r => chrome.storage.local.get([storageKey], d => r(d[storageKey] || '')));
  if (cached) return cached;
  const headers = { 'Authorization': `Bearer ${token}` };
  const res = await fetch(`https://www.wrike.com/api/v4/folders/${TELEMETRY_FOLDER_ID}/tasks`, { headers });
  if (res.ok) {
    const tasks = (await res.json()).data || [];
    const existing = tasks.find(t => t.title === `WAA Log — ${userName}`);
    if (existing) {
      chrome.storage.local.set({ [storageKey]: existing.id });
      return existing.id;
    }
  }
  const createRes = await fetch(`https://www.wrike.com/api/v4/folders/${TELEMETRY_FOLDER_ID}/tasks`, {
    method  : 'POST',
    headers : { ...headers, 'Content-Type': 'application/json' },
    body    : JSON.stringify({ title: `WAA Log — ${userName}` }),
  });
  if (!createRes.ok) return null;
  const newTask = ((await createRes.json()).data || [])[0];
  if (!newTask) return null;
  chrome.storage.local.set({ [storageKey]: newTask.id });
  return newTask.id;
}

async function logTelemetryEvent(event, ctx = {}) {
  try {
    const token = await getWrikeToken();
    if (!token) return;
    const profile  = await getProfile();
    const userName = profile.user_name || 'Unknown User';
    const waaTaskId = await getTelemetryTaskId(token, userName);
    if (!waaTaskId) return;
    const version = chrome.runtime.getManifest().version;
    const now     = new Date();
    const dateStr = now.toISOString().slice(0, 10);
    const timeStr = now.toTimeString().slice(0, 5);

    const lines = [`**${event}** | ${dateStr} ${timeStr} | v${version}`];

    if (ctx.ticketTitle  && TELEMETRY_LOG.ticketTitle)    lines.push(`Ticket: ${ctx.ticketTitle}${TELEMETRY_LOG.ticketId && ctx.ticketId ? ` (#${ctx.ticketId})` : ''}`);
    if (ctx.ticketTitles && TELEMETRY_LOG.ticketTitles)   lines.push(`Tickets: ${ctx.ticketTitles}`);
    if (ctx.ticketStatus && TELEMETRY_LOG.ticketStatus)   lines.push(`Status: ${ctx.ticketStatus}`);
    if (ctx.assignees?.length && TELEMETRY_LOG.ticketAssignees) lines.push(`Assignees: ${ctx.assignees.join(', ')}`);
    if (ctx.dueDate      && TELEMETRY_LOG.ticketDueDate)  lines.push(`Due: ${ctx.dueDate}`);
    if (ctx.dataSource   && TELEMETRY_LOG.dataSource)     lines.push(`Source: ${ctx.dataSource}`);

    const aiParts = [];
    if (ctx.aiProvider   && TELEMETRY_LOG.aiProvider)     aiParts.push(ctx.aiProvider);
    if (ctx.aiModel      && TELEMETRY_LOG.aiModel)        aiParts.push(`(${ctx.aiModel})`);
    if (aiParts.length) lines.push(`AI: ${aiParts.join(' ')}`);

    if (ctx.tone         && TELEMETRY_LOG.tone)           lines.push(`Tone: ${ctx.tone}`);
    if (ctx.tab          && TELEMETRY_LOG.tabGenerated)   lines.push(`Tab: ${ctx.tab}`);
    if (ctx.tabs         && TELEMETRY_LOG.regenTabs)      lines.push(`Tabs regenerated: ${ctx.tabs}`);
    if (ctx.taskCount    && TELEMETRY_LOG.taskCount)      lines.push(`Tasks: ${ctx.taskCount}`);
    if (ctx.question     && TELEMETRY_LOG.chatQuestion)   lines.push(`Question: "${ctx.question}"`);

    await fetch(`https://www.wrike.com/api/v4/tasks/${waaTaskId}/comments`, {
      method  : 'POST',
      headers : { 'Authorization': `Bearer ${token}`, 'Content-Type': 'application/json' },
      body    : JSON.stringify({ text: lines.join('\n') }),
    });
  } catch (_) { /* telemetry must never break the extension */ }
}

