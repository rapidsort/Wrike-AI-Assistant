function applyMyTasksFilters() {
  const listEl   = $('my-tasks-list');
  if (!listEl) return;

  const search     = ($('my-tasks-search')?.value   || '').toLowerCase().trim();
  const priority   = $('my-tasks-priority-select')?.value || '';
  const statusIds  = getSelectedStatusIds();
  let   folderIds  = getSelectedFolderIds();

  // Nothing checked → treat as "all options selected" so pinned folders still gate the list
  if (!folderIds.length) {
    const allFolderCbs = [...document.querySelectorAll('#my-tasks-folder-dropdown input[type="checkbox"]')];
    if (allFolderCbs.length) folderIds = allFolderCbs.map(cb => cb.value);
  }

  const dateFilter = $('my-tasks-date-select')?.value || '';
  const now = Date.now();
  const daysMs = d => d * 86400000;

  let filtered = [...myTasksData.tasks];

  // Date filter — applied before other filters to control which task set is shown
  if (dateFilter.startsWith('modified_')) {
    const days = parseInt(dateFilter.split('_')[1], 10);
    filtered = filtered.filter(t => t.status !== 'Completed' && now - new Date(t.updatedDate).getTime() <= daysMs(days));
  } else if (dateFilter.startsWith('completed_')) {
    const days = parseInt(dateFilter.split('_')[1], 10);
    filtered = filtered.filter(t => {
      if (t.status !== 'Completed') return false;
      const completedAt = t.completedDate ? new Date(t.completedDate).getTime() : null;
      if (!completedAt) return false;
      return now - completedAt <= daysMs(days);
    });
  } else {
    // Hide completed tasks by default — only show them when the user explicitly
    // selects a completed status from the status dropdown.
    filtered = filtered.filter(t => t.status !== 'Completed' || statusIds.includes(t.customStatusId));
  }

  if (search)            filtered = filtered.filter(t => t.title.toLowerCase().includes(search));
  if (priority)          filtered = filtered.filter(t => t.importance === priority);
  if (statusIds.length)  filtered = filtered.filter(t => statusIds.includes(t.customStatusId));
  if (folderIds.length)  filtered = filtered.filter(t => (t.parentIds || []).some(id => folderIds.includes(id)));

  // Sort
  const sortVal = $('my-tasks-sort-select')?.value || 'updated';
  const PRIORITY_ORDER = { Critical: 0, High: 1, Normal: 2, Low: 3 };
  const cfVal = (task, title) =>
    (task.customFields || []).find(f => myTasksData.customFieldDefs[f.id] === title)?.value || '';

  filtered.sort((a, b) => {
    switch (sortVal) {
      case 'updated':  return new Date(b.updatedDate) - new Date(a.updatedDate);
      case 'priority': return (PRIORITY_ORDER[a.importance] ?? 4) - (PRIORITY_ORDER[b.importance] ?? 4);
      case 'status': {
        const sa = myTasksData.statuses[a.customStatusId]?.name || '';
        const sb = myTasksData.statuses[b.customStatusId]?.name || '';
        return sa.localeCompare(sb);
      }
      case 'title':   return a.title.localeCompare(b.title);
      case 'channel': return cfVal(a, cfName(0)).localeCompare(cfVal(b, cfName(0)));
      case 'opttype': return cfVal(a, cfName(2)).localeCompare(cfVal(b, cfName(2)));
      default:        return 0;
    }
  });

  lastFilteredTasks = filtered;

  // Update count bar — show only the visible count; the raw API total is noise when folders are pinned
  const countBar = $('my-tasks-count-bar');
  if (countBar) {
    const showing = filtered.length;
    countBar.textContent = `${showing} task${showing !== 1 ? 's' : ''}`;
  }

  listEl.innerHTML = '';

  if (!filtered.length) {
    listEl.innerHTML = '<div class="history-empty">No tasks match your filters.</div>';
    return;
  }

  const groupBy = $('my-tasks-group-select')?.value || '';

  // Group key extractor
  const getGroupKey = task => {
    switch (groupBy) {
      case 'channel':  return cfVal(task, cfName(0)) || `No ${cfName(0) || 'Channel'}`;
      case 'solution': return cfVal(task, cfName(1)) || `No ${cfName(1) || 'Solution Area'}`;
      case 'folder': {
        const name = (task.parentIds || []).map(id => myTasksData.folders[id]?.title).filter(Boolean).join(', ');
        return name || 'No Folder';
      }
      case 'status': return myTasksData.statuses[task.customStatusId]?.name || 'Unknown Status';
      default: {
        // Dynamic extra fields: value is 'cf-extra-N' where N is the cfName index
        const m = groupBy.match(/^cf-extra-(\d+)$/);
        if (m) {
          const idx = parseInt(m[1], 10);
          return cfVal(task, cfName(idx)) || `No ${cfName(idx) || `Field ${idx + 1}`}`;
        }
        return null;
      }
    }
  };

  const renderCard = (task) => {
    const div = document.createElement('div');
    div.className = 'my-task-card';
    if (starredTaskIds.has(task.id)) div.classList.add('my-task-card--starred');

    const due = task.dates?.due
      ? `Due ${new Date(task.dates.due).toLocaleDateString(undefined, { month: 'short', day: 'numeric' })}`
      : '';
    const imp = task.importance && task.importance !== 'Normal'
      ? `<span class="my-task-imp my-task-imp--${task.importance.toLowerCase()}">${task.importance}</span>`
      : '';

    const alertPriority = (task.importance === 'Normal' || task.importance === 'Low' || !task.importance)
      && PRIORITY_ALERT_RE.test(task.title);
    const priorityAlertBadge = alertPriority
      ? '<span class="my-task-priority-alert" title="Title suggests higher priority may be needed">&#x26A0; Review priority</span>'
      : '';

    const statusInfo = task.customStatusId ? myTasksData.statuses[task.customStatusId] : null;
    const statusStyle = statusInfo?.color ? wrikeStatusStyle(statusInfo.color) : '';
    const statusBadge = statusInfo
      ? `<span class="my-task-status" style="${statusStyle}">${escapeHtml(statusInfo.name)}</span>`
      : '';

    const folderName = (task.parentIds || [])
      .map(id => myTasksData.folders[id]?.title)
      .filter(Boolean)
      .join(', ');

    const accentColor = statusInfo?.color ? wrikeAccentColor(statusInfo.color) : '#94a3b8';
    div.style.borderLeftColor = accentColor;

    const TARGET_CF = customCfNames.filter(Boolean);
    const cfHtml = (task.customFields || [])
      .filter(f => TARGET_CF.includes(myTasksData.customFieldDefs[f.id]) && f.value)
      .map(f => {
        const label = myTasksData.customFieldDefs[f.id];
        const cls   = getCfColorClass(label);
        return `<span class="my-task-cf ${cls}">${escapeHtml(f.value)}</span>`;
      })
      .join('');

    const updated = task.updatedDate
      ? `Updated ${new Date(task.updatedDate).toLocaleDateString(undefined, { month: 'short', day: 'numeric' })}`
      : '';

    div.innerHTML = `
      <div class="my-task-title">${escapeHtml(task.title)}</div>
      <div class="my-task-meta">
        ${imp}
        ${priorityAlertBadge}
        ${statusBadge}
        ${due   ? `<span class="my-task-due">${escapeHtml(due)}</span>`     : ''}
        ${updated ? `<span class="my-task-updated">${escapeHtml(updated)}</span>` : ''}
        ${folderName ? `<span class="my-task-folder">&#x1F4C1; ${escapeHtml(folderName)}</span>` : ''}
      </div>
      ${cfHtml ? `<div class="my-task-fields">${cfHtml}</div>` : ''}`;

    div.addEventListener('click', () => {
      if (tasksCompareModeActive) {
        const idx = tasksCompareSelection.findIndex(s => s.id === task.id);
        if (idx >= 0) {
          tasksCompareSelection.splice(idx, 1);
          div.classList.remove('compare-selected');
          div.querySelector('.compare-order-badge')?.remove();
          document.querySelectorAll('.my-task-card.compare-selected').forEach((el, i) => {
            const badge = el.querySelector('.compare-order-badge');
            if (badge) badge.textContent = i + 1;
          });
        } else if (tasksCompareSelection.length < COMPARE_MAX) {
          tasksCompareSelection.push({ id: task.id, task });
          div.classList.add('compare-selected');
          const badge = document.createElement('span');
          badge.className = 'compare-order-badge';
          badge.textContent = tasksCompareSelection.length;
          div.appendChild(badge);
        }
        const n    = tasksCompareSelection.length;
        const hint = $('my-tasks-compare-hint');
        const run  = $('my-tasks-run-compare-btn');
        if (hint) hint.textContent = n === 0
          ? `Select 2–${COMPARE_MAX} tasks to compare`
          : n === 1
            ? `Select 1 more (up to ${COMPARE_MAX - 1} more)`
            : n < COMPARE_MAX
              ? `${n} selected — add more or click Compare`
              : `${n} selected (max) — click Compare`;
        if (run) run.classList.toggle('hidden', n < 2);
        return;
      }
      chrome.tabs.create({ url: task.permalink });
    });

    // Star button — toggles task in/out of newsletter selection
    const starBtn = document.createElement('button');
    starBtn.className = 'my-task-star-btn';
    starBtn.title = 'Pin for newsletter';
    starBtn.textContent = starredTaskIds.has(task.id) ? '★' : '☆';
    starBtn.addEventListener('click', e => {
      e.stopPropagation();
      if (starredTaskIds.has(task.id)) {
        starredTaskIds.delete(task.id);
        starBtn.textContent = '☆';
        div.classList.remove('my-task-card--starred');
      } else {
        starredTaskIds.add(task.id);
        starBtn.textContent = '★';
        div.classList.add('my-task-card--starred');
      }
      updateNewsletterBtnBadge();
      chrome.storage.session.set({ pinned_task_ids: [...starredTaskIds] });
    });
    div.appendChild(starBtn);

    return div;
  };

  if (!groupBy) {
    filtered.forEach(task => listEl.appendChild(renderCard(task)));
  } else {
    // Group tasks and render with headers
    const groups = new Map();
    filtered.forEach(task => {
      const key = getGroupKey(task);
      if (!groups.has(key)) groups.set(key, []);
      groups.get(key).push(task);
    });
    groups.forEach((tasks, groupName) => {
      const header = document.createElement('div');
      header.className = 'my-task-group-header my-task-group-toggle';
      header.innerHTML = `<span>${escapeHtml(groupName)} (${tasks.length})</span>`;

      const body = document.createElement('div');
      body.className = 'my-task-group-body';
      tasks.forEach(task => body.appendChild(renderCard(task)));

      header.addEventListener('click', () => {
        const collapsed = body.classList.toggle('collapsed');
        header.classList.toggle('is-collapsed', collapsed);
      });

      listEl.appendChild(header);
      listEl.appendChild(body);
    });
  }
}

function formatAge(ts) {
  const diff = Math.floor((Date.now() - ts) / 1000);
  if (diff < 60)   return 'just now';
  if (diff < 3600) return `${Math.floor(diff / 60)}m ago`;
  if (diff < 86400) return `${Math.floor(diff / 3600)}h ago`;
  return `${Math.floor(diff / 86400)}d ago`;
}

// ── Post comment to Wrike ─────────────────────────────────────────────────
async function postCommentToWrike() {
  const commentEl  = $('my-comment');
  const rawMd      = commentEl?.dataset.raw?.trim() || commentEl?.innerText?.trim() || '';
  if (!rawMd) return;
  const commentHtml = markdownToHtml(rawMd);

  const wrikeToken = await getWrikeToken();
  const taskId     = currentTaskData?.taskId;
  if (!wrikeToken || !taskId) return;

  const btn = $('post-comment-btn');
  if (!btn) return;
  btn.textContent = 'Posting…';
  btn.disabled    = true;

  try {
    const res = await fetch(`https://www.wrike.com/api/v4/tasks/${taskId}/comments`, {
      method : 'POST',
      headers: {
        'Authorization': `Bearer ${wrikeToken}`,
        'Content-Type' : 'application/x-www-form-urlencoded',
      },
      body: new URLSearchParams({ text: commentHtml }).toString(),
    });

    if (!res.ok) {
      let msg = `HTTP ${res.status}`;
      try { const e = await res.json(); msg = e?.error?.message || msg; } catch (_) {}
      throw new Error(msg);
    }

    btn.textContent       = '✓ Posted!';
    btn.style.borderColor = '#22c55e';
    btn.style.color       = '#16a34a';
    logTelemetryEvent('POST_COMMENT', { ticketId: currentTaskData?.taskId, ticketTitle: currentTaskData?.title });
    setTimeout(() => {
      btn.textContent       = 'Post to Wrike';
      btn.style.borderColor = '';
      btn.style.color       = '';
      btn.disabled          = false;
    }, 3000);
  } catch (err) {
    btn.textContent       = 'Failed — Retry';
    btn.style.borderColor = '#ef4444';
    btn.style.color       = '#ef4444';
    btn.disabled          = false;
    setTimeout(() => {
      btn.textContent       = 'Post to Wrike';
      btn.style.borderColor = '';
      btn.style.color       = '';
    }, 3000);
  }
}

