// Convert Claude content blocks to OpenAI-compatible content array
function blocksToOpenAI(blocks) {
  if (!Array.isArray(blocks)) return [{ type: 'text', text: String(blocks) }];
  return blocks.map(b => {
    if (b.type === 'text')  return { type: 'text', text: b.text };
    if (b.type === 'image') return { type: 'image_url', image_url: { url: `data:${b.source.media_type};base64,${b.source.data}` } };
    return null;
  }).filter(Boolean);
}

// Convert Claude content blocks to Gemini parts array
function blocksToGemini(blocks) {
  if (!Array.isArray(blocks)) return [{ text: String(blocks) }];
  return blocks.map(b => {
    if (b.type === 'text')     return { text: b.text };
    if (b.type === 'image')    return { inline_data: { mime_type: b.source.media_type, data: b.source.data } };
    if (b.type === 'document') return { inline_data: { mime_type: 'application/pdf', data: b.source.data } };
    return null;
  }).filter(Boolean);
}

// Convert Claude content blocks to Ollama {content, images}
function blocksToOllama(blocks) {
  if (!Array.isArray(blocks)) return { content: String(blocks) };
  const texts  = blocks.filter(b => b.type === 'text').map(b => b.text);
  const images = blocks.filter(b => b.type === 'image').map(b => b.source.data);
  return { content: texts.join('\n\n'), images: images.length ? images : undefined };
}

// Build a request body for text-only (no content-block) calls
function buildTextCallBody(provider, model, systemText, messages, maxTokens) {
  if (provider === 'claude') {
    return { model, max_tokens: maxTokens, stream: true, system: [{ type: 'text', text: systemText }], messages };
  }
  if (provider === 'openai') {
    return { model, max_tokens: maxTokens, stream: true, messages: [{ role: 'system', content: systemText }, ...messages] };
  }
  if (provider === 'gemini') {
    const contents = messages.map(m => ({
      role  : m.role === 'assistant' ? 'model' : 'user',
      parts : [{ text: m.content }],
    }));
    return { system_instruction: { parts: [{ text: systemText }] }, contents, generationConfig: { maxOutputTokens: maxTokens } };
  }
  if (provider === 'ollama') {
    return { model, stream: true, options: { num_predict: maxTokens }, messages: [{ role: 'system', content: systemText }, ...messages] };
  }
}

// Show typing dots on el; returns a function that transitions el to streaming cursor
function startStreamingIndicator(el) {
  if (!el) return () => {};
  el.innerHTML = '<div class="typing-indicator"><span></span><span></span><span></span></div>';
  let transitioned = false;
  return function onFirstToken() {
    if (transitioned) return;
    transitioned = true;
    el.classList.add('ai-streaming');
  };
}
function stopStreamingIndicator(el) {
  if (!el) return;
  el.classList.remove('ai-streaming');
}

// Stream reader for OpenAI SSE — returns { text, finishReason }
async function readOpenAIStream(body, targetEl = null, prependText = '') {
  const reader = body.getReader();
  const dec    = new TextDecoder();
  let newText     = '';
  let finishReason = null;
  let buf = '';
  const onFirst = startStreamingIndicator(targetEl);
  while (true) {
    const { done, value } = await reader.read();
    if (done) break;
    buf += dec.decode(value, { stream: true });
    const lines = buf.split('\n');
    buf = lines.pop();
    for (const line of lines) {
      if (!line.startsWith('data: ')) continue;
      const data = line.slice(6).trim();
      if (data === '[DONE]') continue;
      try {
        const evt   = JSON.parse(data);
        const delta = evt?.choices?.[0]?.delta?.content || '';
        if (delta) {
          onFirst();
          newText += delta;
          if (targetEl) targetEl.innerHTML = renderMarkdown(prependText + newText);
        }
        const fr = evt?.choices?.[0]?.finish_reason;
        if (fr) finishReason = fr;
      } catch (_) {}
    }
  }
  stopStreamingIndicator(targetEl);
  return { text: newText, finishReason };
}

// Stream reader for Gemini SSE — returns full text
async function readGeminiStream(body, targetEl = null, prependText = '') {
  const reader = body.getReader();
  const dec    = new TextDecoder();
  let fullText = '';
  let buf = '';
  const onFirst = startStreamingIndicator(targetEl);
  while (true) {
    const { done, value } = await reader.read();
    if (done) break;
    buf += dec.decode(value, { stream: true });
    const lines = buf.split('\n');
    buf = lines.pop();
    for (const line of lines) {
      if (!line.startsWith('data: ')) continue;
      const data = line.slice(6).trim();
      if (!data) continue;
      try {
        const evt  = JSON.parse(data);
        const text = evt?.candidates?.[0]?.content?.parts?.[0]?.text || '';
        if (text) {
          onFirst();
          fullText += text;
          if (targetEl) targetEl.innerHTML = renderMarkdown(prependText + fullText);
        }
      } catch (_) {}
    }
  }
  stopStreamingIndicator(targetEl);
  return fullText;
}

