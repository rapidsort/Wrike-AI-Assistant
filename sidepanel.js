// ── URL fetch helpers ─────────────────────────────────────────────────────

// Open a Wrike task URL in a background tab, wait for the SPA to render,
// scrape the content using the same function as the main ticket flow, then close the tab.
function fetchWrikeUrlViaTab(url) {
  return new Promise((resolve, reject) => {
    let tabId = null;

    const cleanup = () => {
      if (tabId !== null) { chrome.tabs.remove(tabId).catch(() => {}); tabId = null; }
      bgScrapeTabId = null;
    };
    const fail = (msg) => { cleanup(); reject(new Error(msg)); };

    const timer = setTimeout(
      () => fail('Timed out loading Wrike task (20 s). Make sure you are logged in to Wrike.'),
      20000
    );

    chrome.tabs.create({ url, active: false }, (tab) => {
      if (chrome.runtime.lastError || !tab) {
        clearTimeout(timer);
        return fail('Could not open Wrike task tab: ' + (chrome.runtime.lastError?.message || 'unknown error'));
      }
      tabId = tab.id;
      bgScrapeTabId = tabId; // suppress onActivated/onUpdated re-init while this tab is alive

      const onUpdated = (tid, info) => {
        if (tid !== tabId || info.status !== 'complete') return;
        chrome.tabs.onUpdated.removeListener(onUpdated);

        // Give Wrike's SPA 3.5 s to render the task panel after page load
        setTimeout(() => {
          chrome.scripting.executeScript(
            { target: { tabId }, func: scrapeWrikeTask },
            (results) => {
              clearTimeout(timer);
              cleanup();

              if (chrome.runtime.lastError) {
                return reject(new Error('Could not read Wrike task page: ' + chrome.runtime.lastError.message));
              }

              const scraped = results?.[0]?.result;
              if (!scraped || (!scraped.title && !scraped.rawPageText)) {
                return reject(new Error('No content found on the Wrike task page. Make sure you are logged in to Wrike and the task is accessible.'));
              }

              const lines = [];
              if (scraped.title)                lines.push(`TITLE: ${scraped.title}`);
              if (scraped.status)               lines.push(`STATUS: ${scraped.status}`);
              if (scraped.assignees?.length)    lines.push(`ASSIGNEES: ${scraped.assignees.join(', ')}`);
              if (scraped.dueDate)              lines.push(`DUE DATE: ${scraped.dueDate}`);
              if (scraped.description)          lines.push(`\nDESCRIPTION:\n${scraped.description}`);
              if (scraped.customFields?.length) lines.push(`\nCUSTOM FIELDS:\n${scraped.customFields.join('\n')}`);
              if (scraped.comments?.length)     lines.push(`\nCOMMENTS:\n${scraped.comments.join('\n---\n')}`);

              const text = lines.join('\n').trim() || scraped.rawPageText?.slice(0, 80000) || '';
              if (!text) return reject(new Error('Wrike task loaded but no content extracted — try again.'));

              resolve({ url, title: scraped.title || url, text, type: 'wrike', truncated: false });
            }
          );
        }, 3500);
      };

      chrome.tabs.onUpdated.addListener(onUpdated);
    });
  });
}

async function fetchUrlContent(url) {
  // ── Special case: Wrike ticket URL — use the API instead of raw fetch ────
  // Only match task URLs (open.htm or app.wrike.com with ?id= numeric param)
  const wrikeIdMatch = url.match(/wrike\.com\/(?:open\.htm|.*?\/task).*[?&]id=(\d+)/i)
                    || url.match(/app\.wrike\.com\/.*[?&]id=(\d+)/i);
  if (wrikeIdMatch) {
    const taskId = wrikeIdMatch[1];
    const token  = await getWrikeToken();
    if (token) {
      const data = await getWrikeApiData(taskId, token);
      if (data && !data.error) {
        const lines = [];
        if (data.title)                            lines.push(`TITLE: ${data.title}`);
        if (data.customStatusName || data.status)  lines.push(`STATUS: ${data.customStatusName || data.status}`);
        if (data.importance)                       lines.push(`IMPORTANCE: ${data.importance}`);
        if (data.assignees?.length)                lines.push(`ASSIGNEES: ${data.assignees.join(', ')}`);
        if (data.dueDate)                          lines.push(`DUE DATE: ${data.dueDate}`);
        if (data.folderNames?.length)              lines.push(`FOLDERS: ${data.folderNames.join(', ')}`);
        if (data.description)                      lines.push(`\nDESCRIPTION:\n${data.description}`);
        if (data.customFields?.length)             lines.push(`\nCUSTOM FIELDS:\n${data.customFields.join('\n')}`);
        if (data.comments?.length)                 lines.push(`\nCOMMENTS:\n${data.comments.join('\n---\n')}`);
        return { url, title: data.title || url, text: lines.join('\n').trim(), type: 'wrike', truncated: false };
      }
      // API failed (e.g. numeric permalink ID not accepted) — fall through to tab-based scraping
    }
    // Open in a background tab and scrape via the page (uses browser session / Wrike login)
    return fetchWrikeUrlViaTab(url);
  }

  // ── Regular URL fetch ────────────────────────────────────────────────────
  const controller = new AbortController();
  const timer = setTimeout(() => controller.abort(), 12000);
  let res;
  try {
    res = await fetch(url, { signal: controller.signal });
  } catch (err) {
    throw new Error(err.name === 'AbortError' ? 'Request timed out (12 s).' : err.message);
  } finally {
    clearTimeout(timer);
  }
  if (!res.ok) throw new Error(`HTTP ${res.status} — page could not be loaded.`);

  const ct = res.headers.get('content-type') || '';

  // ── Image URL — fetch as base64 block for Claude ─────────────────────────
  if (ct.startsWith('image/')) {
    const mimeType = ct.split(';')[0].trim();
    const buf    = await res.arrayBuffer();
    const bytes  = new Uint8Array(buf);
    let binary = '';
    bytes.forEach(b => { binary += String.fromCharCode(b); });
    const data     = btoa(binary);
    const filename = new URL(url).pathname.split('/').pop() || 'image';
    return { url, title: filename, text: '', type: 'image', imageData: { mimeType, data } };
  }

  // ── PDF URL — fetch as base64 document block for Claude ──────────────────
  if (ct.includes('application/pdf')) {
    const buf    = await res.arrayBuffer();
    const bytes  = new Uint8Array(buf);
    let binary = '';
    bytes.forEach(b => { binary += String.fromCharCode(b); });
    const data     = btoa(binary);
    const filename = new URL(url).pathname.split('/').pop() || 'document.pdf';
    return { url, title: filename, text: '', type: 'pdf', pdfData: { mimeType: 'application/pdf', data } };
  }

  if (!ct.includes('text/html') && !ct.includes('text/plain')) {
    throw new Error('Only HTML, plain-text, and PDF pages are supported.');
  }

  const html = await res.text();
  const doc  = new DOMParser().parseFromString(html, 'text/html');

  // Remove non-content elements
  ['script','style','nav','footer','header','aside','noscript','iframe'].forEach(tag =>
    doc.querySelectorAll(tag).forEach(el => el.remove())
  );

  const title   = doc.title?.trim() || url;
  const full    = (doc.body?.innerText || doc.body?.textContent || '')
    .split('\n').map(l => l.trim()).filter(l => l.length > 2)
    .join('\n');
  const LIMIT   = 80000;
  const text    = full.slice(0, LIMIT);
  const truncated = full.length > LIMIT;

  if (!text) throw new Error('No readable content found on this page.');
  return { url, title, text, type: 'html', truncated };
}

