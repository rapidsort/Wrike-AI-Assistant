// ── Ticket changed alerts (background check for history items) ────────────
async function checkHistoryForUpdates(historyItems) {
  const token = await getWrikeToken();
  if (!token) return;
  for (const item of historyItems) {
    const cached = await getCachedAnalysis(item.taskId);
    if (!cached?.taskData?.updatedDate) continue;
    try {
      const res = await fetch(
        `https://www.wrike.com/api/v4/tasks/${item.taskId}?fields=${encodeURIComponent(JSON.stringify(['updatedDate']))}`,
        { headers: { 'Authorization': `Bearer ${token}` } }
      );
      if (!res.ok) continue;
      const json        = await res.json();
      const currentDate = json.data?.[0]?.updatedDate;
      if (currentDate && currentDate !== cached.taskData.updatedDate) {
        const el = document.querySelector(`.history-item[data-taskid="${item.taskId}"]`);
        if (el && !el.querySelector('.history-changed-badge')) {
          el.querySelector('.history-item-title')?.insertAdjacentHTML(
            'afterend', '<span class="history-changed-badge">Updated</span>'
          );
        }
      }
    } catch (_) {}
  }
}

