async function readStreamForTab(body, tabId) {
  const reader  = body.getReader();
  const decoder = new TextDecoder();
  let accumulated = '';
  let buf         = '';

  const meta      = tabId ? SECTION_META[tabId] : null;
  const tabEl     = tabId ? $(`tab-${tabId}`) : null;
  const contentEl = tabEl?.querySelector('.content-body');

  while (true) {
    const { done, value } = await reader.read();
    if (done) break;
    buf += decoder.decode(value, { stream: true });
    const lines = buf.split('\n');
    buf = lines.pop();

    for (const line of lines) {
      if (!line.startsWith('data: ')) continue;
      const data = line.slice(6).trim();
      if (data === '[DONE]') continue;
      try {
        const event = JSON.parse(data);
        if (event.type === 'content_block_delta' && event.delta?.type === 'text_delta') {
          accumulated += event.delta.text;
          const words = accumulated.trim().split(/\s+/).length;
          if (tabId) {
            const spanEl = document.querySelector(`#tab-${tabId} .tab-loading span`);
            if (spanEl) spanEl.textContent = `Generating… (${words} words)`;

            // Incremental render for string-valued sections
            if (meta && contentEl && meta.storeKey !== 'next_steps') {
              const partial = extractPartialValue(accumulated, meta.storeKey);
              if (partial) {
                contentEl.classList.remove('hidden');
                contentEl.innerHTML = renderMarkdown(partial);
              }
            }
          }
        }
      } catch (_) {}
    }
  }
  return accumulated;
}

// ── Follow-up chat ────────────────────────────────────────────────────────
async function readStreamForChat(body, displayEl) {
  const reader  = body.getReader();
  const decoder = new TextDecoder();
  let accumulated = '';
  let buf         = '';
  const onFirst   = startStreamingIndicator(displayEl);

  while (true) {
    const { done, value } = await reader.read();
    if (done) break;
    buf += decoder.decode(value, { stream: true });
    const lines = buf.split('\n');
    buf = lines.pop();

    for (const line of lines) {
      if (!line.startsWith('data: ')) continue;
      const data = line.slice(6).trim();
      if (data === '[DONE]') continue;
      try {
        const event = JSON.parse(data);
        if (event.type === 'content_block_delta' && event.delta?.type === 'text_delta') {
          onFirst();
          accumulated += event.delta.text;
          if (displayEl) displayEl.innerHTML = renderMarkdown(accumulated);
        }
      } catch (_) {}
    }
  }
  stopStreamingIndicator(displayEl);
  return accumulated;
}

async function sendFollowup() {
  const input    = $('followup-input');
  const question = input.value.trim();
  if (!question || conversationHistory.length === 0) return;
  logTelemetryEvent('CHAT', {
    ticketId  : currentTaskId,
    ticketTitle: currentTaskData?.title,
    aiProvider: await getAiProvider(),
    aiModel   : cachedClaudeModel,
    question,
  });

  input.value   = '';
  input.style.height = '';
  input.style.overflowY = 'hidden';
  input.disabled = true;
  $('followup-send-btn').disabled = true;

  addChatMessage('user', question);
  const thinkingEl = addChatMessage('assistant', '');

  try {
    const s          = await getAiSettings();
    const profile    = await getProfile();
    const systemText = buildChatSystemPrompt(profile);
    const msgs       = [...chatHistory(conversationHistory), { role: 'user', content: question }];

    let answer = await callAIChat(s, systemText, msgs, 1024, null, thinkingEl);
    if (!answer) answer = '(no response)';

    thinkingEl.innerHTML = renderMarkdown(answer);
    conversationHistory.push({ role: 'user',      content: question });
    conversationHistory.push({ role: 'assistant', content: answer  });
    if (currentTaskData?.taskId) saveChatTurn(currentTaskData.taskId, question, answer);
  } catch (err) {
    thinkingEl.textContent = `Error: ${err.message}`;
  } finally {
    input.disabled = false;
    $('followup-send-btn').disabled = false;
    input.focus();
  }
}

