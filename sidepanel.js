function renderCfFieldList(names) {
  const el = $('sp-cf-field-list');
  if (!el) return;
  el.innerHTML = '';
  const roleLabels = ['Channel', 'Solution Area', 'Opt. Type'];
  names.forEach((name, i) => {
    const row = document.createElement('div');
    row.className = 'sp-cf-row';
    const roleHint = roleLabels[i] ? ` (${roleLabels[i]})` : '';
    const lbl = document.createElement('label');
    lbl.className = 'sp-label';
    lbl.style.marginBottom = '4px';
    lbl.textContent = `Field ${i + 1}${roleHint}`;
    let control;
    if (cachedApiFieldNames.length) {
      // Use a searchable datalist instead of a plain <select> — with thousands of
      // fields a <select> is unusable; native datalist filters as the user types.
      const listId = `cf-datalist-${i}`;
      const datalist = document.createElement('datalist');
      datalist.id = listId;
      cachedApiFieldNames.forEach(n => {
        const opt = document.createElement('option');
        opt.value = n;
        datalist.appendChild(opt);
      });
      control = document.createElement('input');
      control.type = 'text';
      control.className = 'sp-input';
      control.setAttribute('list', listId);
      control.value = name;
      control.placeholder = 'Type to search fields…';
      // Store datalist on the row so it can be appended to the DOM alongside the input
      row._datalist = datalist;
    } else {
      control = document.createElement('input');
      control.type = 'text';
      control.className = 'sp-input';
      control.value = name;
      control.placeholder = roleLabels[i] ? `e.g. ${roleLabels[i]}` : 'Field name…';
    }
    control.dataset.cfIndex = i;
    const removeBtn = document.createElement('button');
    removeBtn.className = 'sp-team-chip-remove';
    removeBtn.title = 'Remove field';
    removeBtn.innerHTML = '&times;';
    removeBtn.style.cssText = 'font-size:16px;margin-left:6px;padding:0 4px;align-self:flex-end;margin-bottom:6px;';
    removeBtn.disabled = names.length <= 1;
    removeBtn.addEventListener('click', () => {
      const cur = readCfRowValues();
      if (cur.length <= 1) return;
      cur.splice(i, 1);
      renderCfFieldList(cur);
    });
    const inner = document.createElement('div');
    inner.style.cssText = 'display:flex;align-items:flex-end;gap:6px;';
    inner.appendChild(control);
    if (row._datalist) inner.appendChild(row._datalist);
    inner.appendChild(removeBtn);
    row.appendChild(lbl); row.appendChild(inner);
    el.appendChild(row);
  });
}

function readCfRowValues() {
  const el = $('sp-cf-field-list');
  if (!el) return [...customCfNames];
  return Array.from(el.querySelectorAll('[data-cf-index]')).map(c => (c.value || '').trim());
}

async function fetchCfFieldsFromApi() {
  const resEl = $('sp-cf-picker-results');
  const btn   = $('sp-cf-load-fields');
  if (!resEl) return;
  resEl.innerHTML = '<p class="hint" style="margin:0;">Loading fields from Wrike…</p>';
  if (btn) { btn.disabled = true; btn.textContent = 'Loading…'; }
  const token = await getWrikeToken();
  if (!token) {
    resEl.innerHTML = '<p class="hint" style="color:#ef4444;margin:0;">No Wrike token — connect Wrike first.</p>';
    if (btn) { btn.disabled = false; btn.textContent = '↺ Load from Wrike'; }
    return;
  }
  try {
    const res = await fetch('https://www.wrike.com/api/v4/customfields',
      { headers: { 'Authorization': `Bearer ${token}` } });
    if (!res.ok) { resEl.innerHTML = `<p class="hint" style="color:#ef4444;margin:0;">Error ${res.status}.</p>`; return; }
    const fields = ((await res.json()).data || []).map(f => f.title).filter(Boolean).sort((a, b) => a.localeCompare(b));
    if (!fields.length) { resEl.innerHTML = '<p class="hint" style="margin:0;">No custom fields found.</p>'; return; }
    cachedApiFieldNames = fields;
    renderCfFieldList(readCfRowValues());
    resEl.innerHTML = `<p class="hint" style="margin:0;">${fields.length} field${fields.length !== 1 ? 's' : ''} loaded — use dropdowns above.</p>`;
  } catch (e) {
    resEl.innerHTML = `<p class="hint" style="color:#ef4444;margin:0;">Network error: ${escapeHtml(e.message)}</p>`;
  } finally {
    if (btn) { btn.disabled = false; btn.textContent = '↺ Load from Wrike'; }
  }
}

