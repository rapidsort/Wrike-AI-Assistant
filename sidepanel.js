// Unified chat call (used by sendFollowup, sendBriefingMessage, generateTabIfNeeded)
async function callAIChat(s, systemText, messages, maxTokens, tabId = null, targetEl = null) {
  const provider = s.ai_provider || 'claude';
  const endpoint = aiProviderEndpoint(s);
  const headers  = aiProviderHeaders(s);

  let body;
  if (provider === 'claude') {
    body = {
      model      : aiProviderModel(s),
      max_tokens : maxTokens,
      stream     : true,
      system     : [{ type: 'text', text: systemText, cache_control: { type: 'ephemeral' } }],
      messages,
    };
  } else {
    // Flatten any content-block messages to strings for non-Claude providers
    const flatMsgs = messages.map(m => {
      if (typeof m.content === 'string') return m;
      if (Array.isArray(m.content)) {
        const texts = m.content.filter(b => b.type === 'text').map(b => b.text).join('\n\n');
        return { role: m.role, content: texts };
      }
      return m;
    });
    body = buildTextCallBody(provider, aiProviderModel(s), systemText, flatMsgs, maxTokens);
  }

  const response = await fetch(endpoint, { method: 'POST', headers, body: JSON.stringify(body) });
  if (!response.ok) {
    let msg = `HTTP ${response.status}`;
    try { const e = await response.json(); msg = e?.error?.message || e?.message || msg; } catch (_) {}
    throw new Error(friendlyApiError(response.status, msg));
  }

  const ct = response.headers.get('content-type') || '';
  if (provider === 'claude') {
    if (ct.includes('text/event-stream')) {
      return tabId ? readStreamForTab(response.body, tabId) : readStreamForChat(response.body, targetEl);
    }
    const payload = await response.json();
    return payload?.content?.[0]?.text || '';
  }
  if (provider === 'openai') return (await readOpenAIStream(response.body, targetEl)).text;
  if (provider === 'gemini') return await readGeminiStream(response.body, targetEl);
  return await readOllamaStream(response.body, targetEl);
}

