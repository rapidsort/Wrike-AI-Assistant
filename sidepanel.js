// ── Copy All ──────────────────────────────────────────────────────────────
function buildCopyAll(analysis, taskData) {
  const parts = [];
  if (taskData?.title) parts.push(`TICKET: ${taskData.title}\n`);

  for (const tabId of TABS) {
    const meta  = SECTION_META[tabId];
    const value = analysis[meta.storeKey];
    if (value === undefined) continue;

    const label = tabId.toUpperCase().replace(/-/g, ' ');
    if (tabId === 'next-steps' && Array.isArray(value)) {
      parts.push(`=== ${label} ===\n` + value.map((s, i) => `${i + 1}. ${stripMarkdown(s)}`).join('\n'));
    } else if (typeof value === 'string' && value) {
      parts.push(`=== ${label} ===\n` + stripMarkdown(value));
    }
  }
  return parts.filter(Boolean).join('\n\n');
}

// ── Prompt builder ────────────────────────────────────────────────────────
function buildUserPrompt(t, extraContext = '') {
  const lines = [];

  if (t.taskId)             lines.push(`TASK ID: ${t.taskId}`);
  if (t.title)              lines.push(`TITLE: ${t.title}`);
  if (t.status)             lines.push(`STATUS: ${t.status}`);
  if (t.importance)         lines.push(`IMPORTANCE: ${t.importance}`);
  if (t.assignees?.length)  lines.push(`ASSIGNEES: ${t.assignees.join(', ')}`);
  if (t.dueDate)            lines.push(`DUE DATE: ${t.dueDate}`);
  if (t.description)        lines.push(`\nDESCRIPTION:\n${t.description}`);
  if (t.customFields?.length) lines.push(`\nCUSTOM FIELDS:\n${t.customFields.join('\n')}`);
  if (t.comments?.length)   lines.push(`\nCOMMENTS (oldest → newest):\n${t.comments.join('\n---\n')}`);
  if (t.attachments?.length) lines.push(`\nATTACHMENTS: ${t.attachments.join(', ')}`);

  // Inline text file contents directly into the prompt
  (t.attachmentContents || [])
    .filter(a => a.category === 'text')
    .forEach(a => lines.push(`\nATTACHMENT "${a.name}":\n${a.data}`));

  // Inline fetched link content — labelled as supporting references (text-only links)
  const textLinks = fetchedLinks.filter(l => l.text);
  if (textLinks.length) {
    lines.push('\n\n--- SUPPORTING REFERENCE MATERIALS ---');
    lines.push('The following resources were fetched from links in or related to the ticket. Use them as supporting context to inform your analysis of the MAIN TICKET above — do not treat them as the primary subject.');
    textLinks.forEach(l => {
      const displayName = l.label ? `${l.label} (${l.title})` : l.title;
      lines.push(`\nREFERENCE: "${displayName}" (${l.url}):\n${l.text}`);
    });
  }
  const imageLinks = fetchedLinks.filter(l => l.imageData);
  if (imageLinks.length) {
    const names = imageLinks.map(l => l.label || l.title).join(', ');
    lines.push(`\n\n[${imageLinks.length} image${imageLinks.length > 1 ? 's' : ''} fetched from URLs included as visual context (${names}) — see attached image block${imageLinks.length > 1 ? 's' : ''} above.]`);
  }
  const pdfLinks = fetchedLinks.filter(l => l.pdfData);
  if (pdfLinks.length) {
    const names = pdfLinks.map(l => l.label || l.title).join(', ');
    lines.push(`\n\n[${pdfLinks.length} PDF${pdfLinks.length > 1 ? 's' : ''} fetched from URLs included as document context (${names}) — see attached document block${pdfLinks.length > 1 ? 's' : ''} above.]`);
  }

  const toneMap = {
    formal        : 'Use a formal, professional tone in all outputs.',
    conversational: 'Use a conversational, natural tone in all outputs.',
    assertive     : 'Use a direct, assertive tone in all outputs — be concise and action-oriented.',
  };
  const toneInstruction = toneMap[selectedTone] || toneMap.conversational;

  const structured = lines.join('\n').trim();
  const supplement = t.rawPageText?.length > 100 ? `\n\nFULL PAGE TEXT (supplementary):\n${t.rawPageText}` : '';
  const body       = structured.length > 80 ? structured + supplement : `RAW PAGE TEXT:\n${t.rawPageText}`;
  const extra      = extraContext ? `\n\nADDITIONAL CONTEXT (email / Teams / notes — use to inform analysis of the main ticket):\n${extraContext}` : '';

  return `Analyze the following Wrike ticket. This is the MAIN ticket — all analysis, summaries, next steps, and drafts should be focused on it. Any reference materials or additional context below are supporting inputs only.\n\nTONE INSTRUCTION: ${toneInstruction}\n\n--- MAIN TICKET ---\n${body}${extra}`;
}

// ── Message block builders ────────────────────────────────────────────────

// Build a content-blocks array for the initial Claude call.
// Ticket text gets cache_control; images and PDFs follow as typed blocks.
function buildMessageBlocks(taskData, ticketText, appendText) {
  const blocks = [
    { type: 'text', text: ticketText, cache_control: { type: 'ephemeral' } },
  ];

  // Wrike API attachment blocks (images / PDFs from the ticket itself)
  for (const att of taskData.attachmentContents || []) {
    if (att.category === 'image') {
      blocks.push({ type: 'image', source: { type: 'base64', media_type: att.mimeType, data: att.data } });
    } else if (att.category === 'document') {
      blocks.push({ type: 'document', source: { type: 'base64', media_type: att.mimeType, data: att.data }, title: att.name });
    }
    // 'text' category already inlined into ticketText via buildUserPrompt
  }

  // User-attached files from the context panel
  for (const att of userAttachments) {
    const displayName = att.label ? `${att.label} (${att.name})` : att.name;
    if (att.category === 'image') {
      blocks.push({ type: 'image', source: { type: 'base64', media_type: att.mimeType, data: att.data } });
    } else if (att.category === 'document') {
      blocks.push({ type: 'document', source: { type: 'base64', media_type: att.mimeType, data: att.data }, title: displayName });
    } else if (att.category === 'text') {
      blocks.push({ type: 'text', text: `\nUSER-ATTACHED FILE "${displayName}":\n${att.data}` });
    }
  }

  // Image blocks from fetched URLs (screenshots, diagrams, etc.)
  // PDF document blocks from fetched URLs
  for (const link of fetchedLinks) {
    if (link.imageData) {
      blocks.push({ type: 'image', source: { type: 'base64', media_type: link.imageData.mimeType, data: link.imageData.data } });
    } else if (link.pdfData) {
      blocks.push({ type: 'document', source: { type: 'base64', media_type: 'application/pdf', data: link.pdfData.data }, title: link.title });
    }
  }

  if (appendText) blocks.push({ type: 'text', text: appendText });
  return blocks;
}

// Strip image/document blobs from conversation history for follow-up calls.
// history[0] is always preserved intact — it carries uploaded files, PDFs,
// fetched links, and ticket content that must stay in context for every call.
// Messages at index 1+ are already text-only (AI responses + follow-up questions).
function textOnlyHistory(history) {
  return history.map((msg, idx) => {
    if (idx === 0) return msg;
    if (typeof msg.content === 'string' || !Array.isArray(msg.content)) return msg;
    const textBlocks = msg.content.filter(b => b.type === 'text');
    if (textBlocks.length === msg.content.length) return msg;
    return { ...msg, content: textBlocks.map(b => b.text).join('\n') || '(content with attachments)' };
  });
}

// Like textOnlyHistory but also unwraps JSON assistant messages so Claude
// doesn't continue the JSON pattern when answering Ask-tab follow-ups.
function chatHistory(history) {
  return textOnlyHistory(history).map(msg => {
    if (msg.role !== 'assistant' || typeof msg.content !== 'string') return msg;
    try {
      const parsed = JSON.parse(msg.content);
      if (parsed && typeof parsed === 'object' && !Array.isArray(parsed)) {
        const text = Object.values(parsed).join('\n\n');
        return { ...msg, content: text };
      }
    } catch (_) {}
    return msg;
  });
}

