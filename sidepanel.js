async function callAI(apiKey, taskData, extraContext = '') {
  const s = await getAiSettings();
  if ((s.ai_provider || 'claude') === 'claude') return callClaude(apiKey, taskData, extraContext);

  const profile      = await getProfile();
  const ticketPrompt = buildUserPrompt(taskData, extraContext);
  const summaryStyle = cachedTabStyles['summary'];
  const sectionReq   = '\n\nReturn ONLY this JSON: {"summary": "..."} — 2–3 sentence overview of what this ticket is about and its current state. Use **bold** for key terms where helpful.'
    + (summaryStyle ? `\n\nStyle instructions: ${summaryStyle}` : '');

  const contentBlocks = buildMessageBlocks(taskData, ticketPrompt, sectionReq);
  const provider      = s.ai_provider;
  const systemText    = buildSystemPrompt(profile);

  // Flatten content blocks to the format each provider expects
  let userContent;
  let ollamaImages;
  if (provider === 'openai') {
    userContent = blocksToOpenAI(contentBlocks);
  } else if (provider === 'gemini') {
    userContent = blocksToGemini(contentBlocks);
  } else {
    const { content, images } = blocksToOllama(contentBlocks);
    userContent  = content;
    ollamaImages = images;
  }

  const body = buildTextCallBody(provider, aiProviderModel(s), systemText,
    [{ role: 'user', content: userContent }],
    SECTION_META['summary'].maxTokens);

  // Ollama: attach images to the user message
  if (provider === 'ollama' && ollamaImages && body.messages) {
    const userMsg = body.messages.find(m => m.role === 'user');
    if (userMsg) userMsg.images = ollamaImages;
  }

  const response = await fetch(aiProviderEndpoint(s), {
    method : 'POST',
    headers: aiProviderHeaders(s),
    body   : JSON.stringify(body),
  });
  if (!response.ok) {
    let msg = `HTTP ${response.status}`;
    try { const e = await response.json(); msg = e?.error?.message || e?.message || msg; } catch (_) {}
    throw new Error(friendlyApiError(response.status, msg));
  }

  let raw;
  if (provider === 'openai') raw = (await readOpenAIStream(response.body)).text;
  else if (provider === 'gemini') raw = await readGeminiStream(response.body);
  else raw = await readOllamaStream(response.body);

  const parsed = parseAIJson(raw);
  if (!parsed) throw new Error('Unexpected response format — hit ↺ Refresh to try again.');

  // Seed conversation history with plain text (no blobs) for non-Claude providers
  const textContent = contentBlocks.filter(b => b.type === 'text').map(b => b.text).join('\n\n');
  conversationHistory = [
    { role: 'user',      content: textContent },
    { role: 'assistant', content: JSON.stringify(parsed) },
  ];
  return parsed;
}

async function callAIText(prompt, targetEl = null, maxTokens = 1024) {
  const s = await getAiSettings();
  if ((s.ai_provider || 'claude') === 'claude') return callClaudeText(prompt, targetEl, maxTokens);

  const provider   = s.ai_provider;
  const profile    = await getProfile();
  const systemText = buildSystemPrompt(profile);
  const model      = aiProviderModel(s);
  const endpoint   = aiProviderEndpoint(s);
  const headers    = aiProviderHeaders(s);

  let fullText     = '';
  let messages     = [{ role: 'user', content: prompt }];
  let canContinue  = true;
  const MAX_CONT   = 5;
  let continuations = 0;

  while (canContinue && continuations <= MAX_CONT) {
    const body     = buildTextCallBody(provider, model, systemText, messages, maxTokens);
    const response = await fetch(endpoint, { method: 'POST', headers, body: JSON.stringify(body) });
    if (!response.ok) {
      let msg = `HTTP ${response.status}`;
      try { const e = await response.json(); msg = e?.error?.message || e?.message || msg; } catch (_) {}
      throw new Error(msg);
    }

    let chunkText;
    let hitLimit = false;

    if (provider === 'openai') {
      const { text, finishReason } = await readOpenAIStream(response.body, targetEl, fullText);
      chunkText = text;
      hitLimit  = finishReason === 'length';
    } else if (provider === 'gemini') {
      chunkText = await readGeminiStream(response.body, targetEl, fullText);
    } else {
      chunkText = await readOllamaStream(response.body, targetEl, fullText);
    }

    fullText += chunkText;
    if (hitLimit) {
      messages = [
        { role: 'user',      content: prompt   },
        { role: 'assistant', content: fullText },
        { role: 'user',      content: 'Continue exactly from where you left off. Do not repeat any text already written.' },
      ];
      continuations++;
    } else {
      canContinue = false;
    }
  }
  return fullText;
}

