function renderSpCustomSections() {
  chrome.storage.sync.get(['custom_sections'], ({ custom_sections }) => {
    const list = $('sp-custom-list');
    if (!list) return;
    const sections = custom_sections || [];
    if (!sections.length) {
      list.innerHTML = '<p class="hint" style="margin-bottom:10px;">No custom tabs yet.</p>';
      return;
    }
    list.innerHTML = '';
    sections.forEach((cs, idx) => {
      const item = document.createElement('div');
      item.className = 'sp-custom-item';
      item.innerHTML = `
        <div style="flex:1;min-width:0;">
          <div class="sp-custom-label">${cs.label}</div>
          <div class="sp-custom-prompt" title="${cs.prompt}">${cs.prompt}</div>
        </div>
        <button class="sp-edit-btn"   data-idx="${idx}">Edit</button>
        <button class="sp-delete-btn" data-idx="${idx}">Delete</button>`;
      list.appendChild(item);
    });
    list.querySelectorAll('.sp-edit-btn').forEach(btn => {
      btn.addEventListener('click', () => {
        const i  = Number(btn.dataset.idx);
        const cs = sections[i];
        const nameEl   = $('sp-new-tab-name');
        const promptEl = $('sp-new-tab-prompt');
        const addBtn   = $('sp-add-tab');
        const cancelBtn = $('sp-cancel-edit');
        if (nameEl)   nameEl.value   = cs.label;
        if (promptEl) promptEl.value = cs.prompt;
        if (addBtn)   addBtn.textContent = 'Save Changes';
        if (cancelBtn) cancelBtn.classList.remove('hidden');
        spEditingIdx = i;
        nameEl?.focus();
        // scroll form into view
        addBtn?.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
      });
    });
    list.querySelectorAll('.sp-delete-btn').forEach(btn => {
      btn.addEventListener('click', () => {
        const newSections = sections.filter((_, i) => i !== Number(btn.dataset.idx));
        chrome.storage.sync.set({ custom_sections: newSections }, () => {
          renderSpCustomSections();
          applyCustomSections();
        });
      });
    });
  });
}

