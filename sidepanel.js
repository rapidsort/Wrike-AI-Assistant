// ── Bulk summary & comparison: simple Claude text call ────────────────────
async function callClaudeText(prompt, targetEl = null, maxTokens = 1024) {
  const apiKey      = await getApiKey();
  const apiEndpoint = await getProxyUrl();
  const profile     = await getProfile();
  const headers = {
    'Content-Type'     : 'application/json',
    'Authorization'    : `Bearer ${apiKey}`,
    'anthropic-version': '2023-06-01',
  };
  const system = [{ type: 'text', text: buildSystemPrompt(profile) }];

  let fullText  = '';
  let messages  = [{ role: 'user', content: prompt }];
  let stopReason = 'max_tokens';
  const MAX_CONTINUATIONS = 5;
  let continuations = 0;
  const onFirst = startStreamingIndicator(targetEl);

  while (stopReason === 'max_tokens' && continuations <= MAX_CONTINUATIONS) {
    const response = await fetch(apiEndpoint, {
      method  : 'POST',
      headers,
      body: JSON.stringify({
        model      : cachedClaudeModel,
        max_tokens : maxTokens,
        stream     : true,
        system,
        messages,
      }),
    });

    if (!response.ok) {
      let msg = `HTTP ${response.status}`;
      try { const e = await response.json(); msg = e?.error?.message || msg; } catch (_) {}
      throw new Error(msg);
    }

    let chunkText = '';
    stopReason    = 'end_turn';
    const reader  = response.body.getReader();
    const dec     = new TextDecoder();

    while (true) {
      const { done, value } = await reader.read();
      if (done) break;
      const chunk = dec.decode(value, { stream: true });
      for (const line of chunk.split('\n')) {
        if (!line.startsWith('data: ')) continue;
        const data = line.slice(6).trim();
        if (data === '[DONE]') continue;
        try {
          const evt = JSON.parse(data);
          const delta = evt?.delta?.text || evt?.content_block?.text || '';
          if (delta) {
            onFirst();
            chunkText += delta;
            fullText  += delta;
            if (targetEl) targetEl.innerHTML = renderMarkdown(fullText);
          }
          if (evt?.type === 'message_delta' && evt?.delta?.stop_reason) {
            stopReason = evt.delta.stop_reason;
          }
        } catch (_) {}
      }
    }

    if (stopReason === 'max_tokens') {
      // Append what we got so far, then ask Claude to continue
      messages = [
        { role: 'user',      content: prompt     },
        { role: 'assistant', content: fullText   },
        { role: 'user',      content: 'Continue exactly from where you left off. Do not repeat any text already written.' },
      ];
      continuations++;
    }
  }
  stopStreamingIndicator(targetEl);
  return fullText;
}

