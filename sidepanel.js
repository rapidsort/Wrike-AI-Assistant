function updateContextSizeIndicator() {
  const el = $('context-size-indicator');
  if (!el) return;

  // Token estimation: text chars ÷ 4, binaries ~1500 tokens each
  let tokens = 0;
  tokens += Math.round(getExtraContextValue().length / 4);
  for (const l of fetchedLinks) tokens += l.text ? Math.round(l.text.length / 4) : 1500;
  for (const a of userAttachments) {
    tokens += (a.category === 'image' || a.category === 'document') ? 1500 : Math.round((a.data || '').length / 4);
  }

  const MAX = 16000; // comfortable context budget shown to user
  const pct = Math.min(100, Math.round((tokens / MAX) * 100));
  const color = pct < 60 ? '#22a65e' : pct < 85 ? '#d97706' : '#ef4444';

  if (tokens === 0) { el.innerHTML = ''; return; }

  el.innerHTML = `
    <div class="ctx-size-row">
      <span class="ctx-size-label">Context: ~${tokens < 1000 ? tokens : (tokens / 1000).toFixed(1) + 'k'} tokens</span>
      <span class="ctx-size-pct" style="color:${color}">${pct}%</span>
    </div>
    <div class="ctx-size-bar-bg"><div class="ctx-size-bar-fill" style="width:${pct}%;background:${color}"></div></div>`;
}

// ── Tab state management ──────────────────────────────────────────────────
// States: 'pending' | 'loading' | 'error' | 'done'
function setTabState(tabId, state, message = '') {
  const tabEl = $(`tab-${tabId}`);
  if (!tabEl) return;

  const contentBody = tabEl.querySelector('.content-body');
  const copyBar     = tabEl.querySelector('.copy-bar');
  let   loadingEl   = tabEl.querySelector('.tab-loading');
  let   pendingEl   = tabEl.querySelector('.tab-pending');

  // Reset overlays
  if (loadingEl) loadingEl.classList.add('hidden');
  if (pendingEl) pendingEl.classList.add('hidden');

  if (state === 'loading') {
    if (!loadingEl) {
      loadingEl = document.createElement('div');
      loadingEl.className = 'tab-loading';
      loadingEl.innerHTML = '<div class="tab-spinner"></div><span>Generating…</span>';
      tabEl.appendChild(loadingEl);
    }
    loadingEl.classList.remove('hidden');
    contentBody?.classList.add('hidden');
    copyBar?.classList.add('hidden');

  } else if (state === 'pending' || state === 'error') {
    if (!pendingEl) {
      pendingEl = document.createElement('div');
      pendingEl.className = 'tab-pending';
      tabEl.appendChild(pendingEl);
    }
    pendingEl.classList.remove('hidden');
    pendingEl.textContent  = message;
    pendingEl.style.color  = state === 'error' ? '#ef4444' : '';
    contentBody?.classList.add('hidden');
    copyBar?.classList.add('hidden');

  } else if (state === 'done') {
    contentBody?.classList.remove('hidden');
    copyBar?.classList.remove('hidden');
  }
}

