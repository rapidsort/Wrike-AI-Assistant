  $('sp-add-tab')?.addEventListener('click', () => {
    const nameEl   = $('sp-new-tab-name');
    const promptEl = $('sp-new-tab-prompt');
    const label    = nameEl?.value.trim();
    const prompt   = promptEl?.value.trim();
    if (!label)  { spStatus('sp-tab-add-status', 'Enter a tab name.', true); return; }
    if (!prompt) { spStatus('sp-tab-add-status', 'Enter a prompt.',   true); return; }

    const id       = spLabelToId(label);
    const storeKey = id.replace(/-/g, '_');
    const BUILTIN_IDS  = new Set(['ask', 'summary', 'next-steps', 'my-comment', 'meeting-request', 'meeting-script']);
    const BUILTIN_KEYS = new Set(['summary', 'next_steps', 'my_comment', 'meeting_request', 'meeting_script']);

    if (BUILTIN_IDS.has(id)) {
      spStatus('sp-tab-add-status', `"${label}" conflicts with a built-in tab.`, true); return;
    }
    if (BUILTIN_KEYS.has(storeKey)) {
      spStatus('sp-tab-add-status', `"${label}" conflicts with a built-in section key.`, true); return;
    }

    const editIdx = spEditingIdx;

    chrome.storage.sync.get(['custom_sections'], ({ custom_sections }) => {
      const sections = custom_sections || [];

      if (editIdx >= 0) {
        // Edit mode — replace the entry, preserving original id/storeKey if label unchanged
        const newId = spLabelToId(label);
        // Check duplicate against other entries only
        if (sections.some((s, i) => i !== editIdx && (s.id === newId || s.label.toLowerCase() === label.toLowerCase()))) {
          spStatus('sp-tab-add-status', 'A tab with that name already exists.', true); return;
        }
        sections[editIdx] = { id: newId, label, prompt };
        chrome.storage.sync.set({ custom_sections: sections }, () => {
          nameEl.value   = '';
          promptEl.value = '';
          $('sp-add-tab').textContent = '+ Add Tab';
          $('sp-cancel-edit')?.classList.add('hidden');
          spEditingIdx = -1;
          spStatus('sp-tab-add-status', 'Tab updated!');
          renderSpCustomSections();
          applyCustomSections();
        });
        return;
      }

      // Add mode
      if (sections.some(s => s.id === id || s.label.toLowerCase() === label.toLowerCase())) {
        spStatus('sp-tab-add-status', 'A tab with that name already exists.', true); return;
      }
      sections.push({ id, label, prompt });
      chrome.storage.sync.set({ custom_sections: sections }, () => {
        nameEl.value   = '';
        promptEl.value = '';
        spStatus('sp-tab-add-status', 'Tab added!');
        renderSpCustomSections();
        applyCustomSections();
      });
    });
  });

