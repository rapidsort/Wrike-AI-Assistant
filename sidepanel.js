async function fetchSpaceFolders(spaceName) {
  if (!spaceName) return { folders: null, debug: 'no space name configured' };
  const token = await getWrikeToken();
  if (!token) return { folders: null, debug: 'no Wrike token' };

  const BASE    = 'https://www.wrike.com/api/v4';
  const headers = { 'Authorization': `Bearer ${token}` };

  try {
    const spacesRes = await fetch(`${BASE}/spaces`, { headers });
    if (!spacesRes.ok) return { folders: null, debug: `GET /spaces → HTTP ${spacesRes.status}` };

    const spaces = (await spacesRes.json()).data || [];
    const titles = spaces.map(s => s.title).join(', ');
    const space  = spaces.find(s => s.title?.toLowerCase() === spaceName.toLowerCase());
    if (!space) return { folders: null, debug: `space not found. Available: ${titles || '(none)'}` };

    async function fetchAllPages(url) {
      let all = [], pageToken = null, pages = 0;
      do {
        const u   = pageToken ? `${url}&nextPageToken=${encodeURIComponent(pageToken)}` : url;
        const res = await fetch(u, { headers });
        if (!res.ok) return { data: null, status: res.status };
        const json = await res.json();
        all = all.concat(json.data || []);
        pageToken = json.nextPageToken || null;
        pages++;
      } while (pageToken && pages < 10);
      return { data: all, status: 200 };
    }

    // Attempt 1: GET /spaces/{id}/folders (no extra params — fields=["project"] causes 400)
    const a1 = await fetchAllPages(`${BASE}/spaces/${space.id}/folders`);
    if (a1.data?.length) return { folders: a1.data.filter(f => f.title), debug: null };
    const debug1 = `GET /spaces/${space.id}/folders → HTTP ${a1.status}, ${a1.data?.length ?? 'null'} items`;

    // Attempt 2: same endpoint with descendants=true to pull nested folders too
    const a2 = await fetchAllPages(`${BASE}/spaces/${space.id}/folders?descendants=true`);
    if (a2.data?.length) return { folders: a2.data.filter(f => f.title), debug: null };
    const debug2 = `descendants=true → HTTP ${a2.status}, ${a2.data?.length ?? 'null'} items`;

    return { folders: [], debug: `space id=${space.id} | ${debug1} | ${debug2}` };
  } catch (err) {
    return { folders: null, debug: `exception: ${err.message}` };
  }
}

async function fetchMyWrikeTasks(userId = null) {
  const token = await getWrikeToken();
  if (!token) return { error: 'no-token' };

  const BASE    = 'https://www.wrike.com/api/v4';
  const headers = { 'Authorization': `Bearer ${token}` };

  try {
    let targetId = userId;
    let userName = '';

    if (!targetId) {
      const storedId = await new Promise(r => chrome.storage.sync.get(['my_wrike_contact_id'], d => r(d.my_wrike_contact_id || '')));
      if (storedId) {
        targetId = storedId;
        userName = myTasksData.contacts.find(m => m.id === storedId)?.name?.split(' ')[0] || '';
      } else {
        const meRes = await fetch(`${BASE}/contacts?me=true`, { headers });
        if (meRes.status === 401) return { error: 'unauthorized' };
        if (!meRes.ok) {
          const e = await meRes.json().catch(() => ({}));
          return { error: 'fetch-failed', detail: `contacts: HTTP ${meRes.status} — ${e?.error?.message || meRes.statusText}` };
        }
        const me = (await meRes.json()).data?.[0];
        if (!me?.id) return { error: 'no-user', detail: 'contacts?me=true returned no data — token may be a service account' };
        targetId = me.id;
        userName = me.firstName || '';
      }
    }

    // Fetch all pages of assigned tasks
    const taskFields = encodeURIComponent(JSON.stringify(['parentIds', 'customFields', 'followerIds']));
    const baseUrl = `${BASE}/tasks?responsibles=${encodeURIComponent(JSON.stringify([targetId]))}&sortField=UpdatedDate&sortOrder=Desc&pageSize=100&fields=${taskFields}`;
    let allTasks = [];
    let pageToken = null;
    let pages = 0;

    do {
      const url      = pageToken ? `${baseUrl}&nextPageToken=${encodeURIComponent(pageToken)}` : baseUrl;
      const tasksRes = await fetch(url, { headers });
      if (!tasksRes.ok) {
        const e = await tasksRes.json().catch(() => ({}));
        return { error: 'fetch-failed', detail: `tasks: HTTP ${tasksRes.status} — ${e?.error?.message || tasksRes.statusText}` };
      }
      const tasksJson = await tasksRes.json();
      allTasks = allTasks.concat(tasksJson.data || []);
      pageToken = tasksJson.nextPageToken || null;
      pages++;
    } while (pageToken && pages < 10);  // cap at 1000 tasks

    const tasks = allTasks.filter(t => t.status !== 'Cancelled');

    return { tasks, userName };
  } catch (err) {
    return { error: err.message };
  }
}

// ── Filter presets ────────────────────────────────────────────────────────
const PRESET_STORAGE_KEY = 'my_tasks_presets';
const PRESET_MAX = 6;

function getFilterPresets() {
  return new Promise(r => chrome.storage.sync.get([PRESET_STORAGE_KEY], d => r(d[PRESET_STORAGE_KEY] || [])));
}

async function saveCurrentPreset() {
  const input    = $('my-tasks-preset-name-input');
  const errorEl  = $('preset-save-error');
  const saveBtn  = $('my-tasks-save-preset-btn');

  if (!input) return;

  // First click: show input and focus it; change button to Confirm
  if (input.style.display === 'none') {
    input.style.display = 'inline-block';
    input.value = '';
    input.focus();
    saveBtn.textContent = 'Confirm';
    if (errorEl) { errorEl.style.display = 'none'; errorEl.textContent = ''; }
    return;
  }

  // Second click (Confirm): validate and save
  const label = input.value.trim().slice(0, 20);
  if (!label) {
    if (errorEl) { errorEl.textContent = 'Enter a name first.'; errorEl.style.display = 'inline'; }
    input.focus();
    return;
  }

  const presets = await getFilterPresets();
  if (presets.length >= PRESET_MAX && !presets.find(p => p.name === label)) {
    if (errorEl) { errorEl.textContent = `Max ${PRESET_MAX} presets — delete one first.`; errorEl.style.display = 'inline'; }
    return;
  }

  const preset = {
    name         : label,
    priority     : $('my-tasks-priority-select')?.value || '',
    statusIds    : getSelectedStatusIds(),
    folderIds    : getSelectedFolderIds(),
    assigneeVals : [...document.querySelectorAll('#my-tasks-assignee-dropdown input[type="checkbox"]:checked')].map(cb => cb.value),
    sortVal      : $('my-tasks-sort-select')?.value     || 'updated',
    groupBy      : $('my-tasks-group-select')?.value    || '',
  };

  const updated = presets.filter(p => p.name !== label);
  updated.push(preset);
  await new Promise(r => chrome.storage.sync.set({ [PRESET_STORAGE_KEY]: updated }, r));

  // Reset UI
  input.style.display  = 'none';
  input.value          = '';
  saveBtn.textContent  = '＋ Save preset';
  if (errorEl) { errorEl.style.display = 'none'; errorEl.textContent = ''; }
  renderFilterPresets();
}

async function deleteFilterPreset(name) {
  const presets = await getFilterPresets();
  const updated = presets.filter(p => p.name !== name);
  await new Promise(r => chrome.storage.sync.set({ [PRESET_STORAGE_KEY]: updated }, r));
  renderFilterPresets();
}

async function applyPreset(preset) {
  const setVal = (id, val) => { const el = $(id); if (el) el.value = val; };
  setVal('my-tasks-priority-select', preset.priority || '');
  setVal('my-tasks-sort-select',     preset.sortVal  || 'updated');
  setVal('my-tasks-group-select',    preset.groupBy  || '');

  const sIds = preset.statusIds?.length
    ? preset.statusIds
    : (preset.statusId ? [preset.statusId] : []);
  const fIds = preset.folderIds?.length
    ? preset.folderIds
    : (preset.folderId ? [preset.folderId] : []);

  const restoreStatusAndFolder = () => {
    const statusDrop = $('my-tasks-status-dropdown');
    if (statusDrop) {
      statusDrop.querySelectorAll('input[type="checkbox"]').forEach(cb => { cb.checked = sIds.includes(cb.value); });
      updateStatusBtnLabel();
    }
    const folderDrop = $('my-tasks-folder-dropdown');
    if (folderDrop) {
      folderDrop.querySelectorAll('input[type="checkbox"]').forEach(cb => { cb.checked = fIds.includes(cb.value); });
      updateFolderBtnLabel();
    }
  };

  // Restore assignee selection then reload tasks
  // Old presets without assigneeVals default to "Me" only
  const aVals = preset.assigneeVals?.length ? preset.assigneeVals : ['__me__'];
  const assigneeDrop = $('my-tasks-assignee-dropdown');
  if (assigneeDrop) {
    assigneeDrop.querySelectorAll('input[type="checkbox"]').forEach(cb => {
      cb.checked = aVals.includes(cb.value);
    });
    updateAssigneeBtnLabel();
    await reloadForAssignees();
    // Restore status/folder after reload rebuilds the dropdowns
    restoreStatusAndFolder();
    applyMyTasksFilters();
  } else {
    restoreStatusAndFolder();
    applyMyTasksFilters();
  }
}

async function renderFilterPresets() {
  const chipsEl = $('my-tasks-preset-chips');
  if (!chipsEl) return;
  const presets = await getFilterPresets();
  chipsEl.innerHTML = '';
  presets.forEach(preset => {
    const chip = document.createElement('span');
    chip.className = 'my-tasks-preset-chip';
    chip.innerHTML = `${escapeHtml(preset.name)}<button class="my-tasks-preset-chip-del" data-name="${escapeHtml(preset.name)}" title="Delete">&times;</button>`;
    chip.addEventListener('click', e => {
      if (e.target.classList.contains('my-tasks-preset-chip-del')) {
        deleteFilterPreset(preset.name);
      } else {
        applyPreset(preset);
      }
    });
    chipsEl.appendChild(chip);
  });
}

